function getContractAddress() {
  const address = import.meta.env.VITE_CONTRACT_ADDRESS;
  if (!address || address === '0xYourDeployedContractAddress') {
    throw new Error('VITE_CONTRACT_ADDRESS not configured in .env');
  }
  return address;
}

